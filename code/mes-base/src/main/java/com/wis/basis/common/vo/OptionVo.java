package com.wis.basis.common.vo;

public class OptionVo {
	private Integer id;
	private String code;
	private String name;

	
	public OptionVo() {
	}
	public OptionVo(String code,String name) {
		this.code=code;
		this.name=name;
	}
	public OptionVo(Integer id,String code,String name) {
		this(code,name);
		this.id=id;
	}
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
